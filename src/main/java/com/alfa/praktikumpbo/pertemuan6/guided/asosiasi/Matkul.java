package com.alfa.praktikumpbo.pertemuan6.guided.asosiasi;

/**
 *
 * @author HP
 */
public class Matkul {
    private String kdMatkul;
    private String namaMatkul;
    
    public Matkul(String kdMatkul, String namaMatkul){
        this.kdMatkul = namaMatkul;
        this.kdMatkul = namaMatkul;
    }

    public String getKdMatkul() {
        return this.kdMatkul;
    }

    public void setNamaMatkul(String NmMakul) {
        this.namaMatkul = NmMakul;
    }

    public String getNamaMatkul() {
        return this.namaMatkul;
    }

    public void setKdBuku(String KdMakul) {
        this.kdMatkul = KdMakul;
    }
}
